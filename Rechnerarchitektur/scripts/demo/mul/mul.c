unsigned long mul_unsigned_long (unsigned long a, unsigned long b) {
  return a * b;
}

short mul_short (short a, short b) { return a * b; }

unsigned short mul_unsigned_short (unsigned short a, unsigned short b) {
  return a * b;
}
